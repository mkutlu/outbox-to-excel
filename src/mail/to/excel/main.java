   public static void check(String host, String storeType, String user,
      String password) 
   {
	   Scanner key = new Scanner(System.in);
	  ArrayList<String> addresses = new ArrayList<>();
      try {

      //create properties field
    	  Properties props = System.getProperties();
    	  props.setProperty("mail.store.protocol", "imaps");
    	  Session session = Session.getDefaultInstance(props, null);
    	  Store store = session.getStore("imaps");
    	  store.connect(host, user, password);
    	  System.out.println(store);


      //create the folder object and open it
      
      Folder[] f = store.getDefaultFolder().list("*");
      for(Folder fd:f) {
         fd.getFullName();
      }
      int choose = key.nextInt();
      Folder emailFolder = store.getFolder(f[choose-1].getFullName().toString());
      emailFolder.open(Folder.READ_ONLY);
      // retrieve the messages from the folder in an array and print it
      Message[] messages = emailFolder.getMessages();
      for (int i = 0, n = messages.length; i < n; i++) {
         Message message = messages[i];
         System.out.println("---------------------------------");
         System.out.println("Email Number " + (i + 1));
         System.out.println("Subject: " + message.getSubject());
         try {
         for ( int m=0; m<message.getAllRecipients().length;m++) {
             addresses.add(message.getAllRecipients()[m].toString());
         }
         }catch(Exception e) {
        	 
         }
       
         System.out.println("Text: " + message.getContent().toString());
      }
      //close the store and folder objects
      //emailFolder.close(false);
      store.close();

      } catch (NoSuchProviderException e) {
         e.printStackTrace();
      } catch (MessagingException e) {
         e.printStackTrace();
      } catch (Exception e) {
         e.printStackTrace();
      }
      
      XSSFWorkbook workbook = new XSSFWorkbook();
      XSSFSheet sheet = workbook.createSheet("receiverAddresses");
      
      int rowNum = 0;
      System.out.println("Creating excel");

      for (String element : addresses) {
          Row row = sheet.createRow(rowNum++);
          int colNum = 0;
          Cell cell = row.createCell(colNum);
          cell.setCellValue(element);
      }

      try {
          FileOutputStream outputStream = new FileOutputStream("C:\\Users\\MesutKutlu\\Desktop\\MyFirstExcel.xlsx");
          workbook.write(outputStream);
          workbook.close();
      } catch (FileNotFoundException e) {
          e.printStackTrace();
      } catch (IOException e) {
          e.printStackTrace();
      }
   }

